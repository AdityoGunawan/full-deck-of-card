package main

import (
	"fmt"
	"strings"
)

func full_decks_count(decks_of_card []string) int {
	var count int
	var card = []string{"2", "3", "4", "5", "6", "7", "8", "9", "T", "J", "Q", "K", "A"}
	var type_card = []string{"S", "C", "D", "H"}
	for _, v := range card {
		for _, v1 := range type_card {
			for _, v2 := range decks_of_card {
				if strings.Contains(v2, v) && strings.Contains(v2, v1) {
					count++
				}
			}
		}
	}
	if count < 52 {
		return 0
	}
	return count / 52
}

